inline val MarkupNode.prevSibling get() = siblings?.let { s -> s.indexOf(this).takeIf { it > 0 }?.let { s[it-1] } }
inline val MarkupNode.nextSibling get() = siblings?.let { s -> s.indexOf(this).takeIf { it >= 0 && it < s.lastIndex }?.let { s[it+1] } }
