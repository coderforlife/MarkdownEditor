inline val MarkupNode.indexInParentOfTag get() = siblings?.filter { it.tag == tag }?.indexOf(this) ?: -1
inline fun MarkupNode.indexInParentOfTags(vararg tags: String) = siblings?.filter { it.tag in tags }?.indexOf(this) ?: -1

