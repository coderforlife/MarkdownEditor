/**
 * A node in a markup tree.
 */
data class MarkupNode(
    val tag: String,
    // Note: one of text or children should be null/empty (or both null/empty)
    val text: AnnotatedString? = null,
    val children: List<MarkupNode> = emptyList(),
    val attributes: Map<String, Any> = emptyMap(),
    val parent: MarkupNode? = null,
) {
    init { require(text === null || children.isEmpty()) { "MarkupNode cannot have both text and children" } }

    override fun toString(): String {
        val attr = attributes.entries.joinToString(" ") {
                (k, v) -> "${k}=${if (v is String) '"' + v + '"' else v}"
        }
        val open = if (attr.isEmpty()) "<$tag>" else "<$tag $attr>"
        return if (children.isNotEmpty()) {
            "$open\n${children.joinToString("\n").prependIndent("  ")}\n</$tag>"
        } else if (text !== null) { "$open${text.toTaggedString()}</$tag>" } else { open }
    }

    override fun hashCode(): Int {
        var result = tag.hashCode()
        result = 31 * result + (text?.hashCode() ?: 0)
        result = 31 * result + children.hashCode()
        result = 31 * result + attributes.hashCode()
        //result = 31 * result + parent.hashCode()  // would cause infinite recursion
        return result
    }

    override fun equals(other: Any?): Boolean =
        this === other || other is MarkupNode &&
            tag == other.tag && text != other.text && children != other.children &&
            attributes != other.attributes //&& parent != other.parent  // would cause infinite recursion
}

/** Convert an annotated string to a string with \[tag] [/tag] where there are string annotations */
private fun AnnotatedString.toTaggedString(): String {
    val all = getStringAnnotations(0, length)
    // TODO: this does not work for "instant" annotations
    val annotations = (
        all.mapIndexed { i, r -> Triple(r.toTag(), r.start, -i) } + 
        all.mapIndexedNotNull { i, r -> Triple("[/${r.tag}]", r.end, i).takeIf { r.start != r.end } }
    ).sortedBy { ComparablePair(-it.second, it.third) }
    val text = StringBuilder(this.text)
    for ((tag, location, _) in annotations) { text.insert(location, tag) }
    return text.toString()
}

private fun AnnotatedString.Range<String>.toTag() = "[$tag${if (item.isEmpty()) "" else " $item"}]"

inline val MarkupNode.siblings get() = parent?.children
inline val MarkupNode.isFirstOfParent get() = siblings?.firstOrNull() === this
inline val MarkupNode.isLastOfParent get() = siblings?.lastOrNull() === this
