fun MarkupNode.toHTML(): String {
    if (tag in specialTags) {
        return when (tag) {
            "#text" -> text?.toString()?.let { encodeHTMLEntities(it) } ?: ""
            "#cdata-section" -> "<![CDATA[${text?.toString()}]]>"
            "#comment" -> "<!--${attributes["data"]?.toString()?.replace("--", "__") ?: ""}-->"
            "#declaration" -> "<?${attributes["data"]?.toString() ?: ""}?>"
            "#processing-instruction" -> "<?${attributes["data"]?.toString() ?: ""}?>"
            else -> error("unknown special tag $tag")
        }
    }

    val attr = attributes.entries
        .filter { (k, v) -> !k.startsWith("*") && (v is String || v is Number || v is Boolean) }
        .joinToString(" ") { it.toAttribute() }
    val open = if (attr.isEmpty()) "<$tag>" else "<$tag $attr>"
    val children = this.children.filter { !it.tag.startsWith("*") }
    return if (children.isEmpty()) {
        if (text === null) open else "$open${text.toHTMLString()}</$tag>"
    } else {
        val contents = children.joinToString("\n") { it.toHTML() }
        "$open\n${contents.prependIndent("  ")}\n</$tag>"
    }
}

private fun AnnotatedString.toHTMLString(): String {
    val all = getStringAnnotations(0, length).filter { it.tag.firstOrNull() != '*' }

    // key: annotation location (start or end)
    // value: list of annotations at that position and their order (non-positive for starts, positive for ends)
    val allAnnotations = (
            all.mapIndexed { i, a -> Triple(a, a.start, -i) } +
            all.mapIndexed { i, a -> Triple(a, a.end, i+1) }
        ).groupBy({ (_, l, _) -> l }) { (a, _, i) -> a to i }


    var last = 0 // last processed position for text
    var skipUntil = 0
    val output = StringBuilder(text.length)
    for (index in allAnnotations.keys.sorted()) {
        // Add all text since the last annotation position
        output.appendHTML(text, max(last, skipUntil), index)

        // Go through all annotations at this position, sorted by their order
        val annotations = allAnnotations.getValue(index)
        for ((a, i) in annotations.sortedBy { -it.second }) {
            if (i <= 0) {
                when (a.tag) {
                    "#text" -> Unit // do nothing, let it process normally
                    "#comment" -> output.append("<!--${a.attrs["data"]?.replace("--", "__") ?: ""}-->")
                    "#declaration" -> output.append("<!${a.attrs["data"] ?: ""}>")
                    "#processing-instruction" -> output.append("<?${a.attrs["data"] ?: ""}?>")
                    "#cdata-section" -> {
                        output.append("<![CDATA[${text.substring(a.start, a.end)}]]>")
                        skipUntil = a.end
                    }
                    in tagHasAltText -> {
                        output.append(a.toTag(text.substring(a.start, a.end), true))
                        skipUntil = a.end
                    }
                    else -> output.append(a.toTag(close = a.start == a.end-1))
                }
            } else if (a.tag !in tagHasAltText && a.tag !in voidTags && a.tag !in specialTags) {
                output.append("</${a.tag}>")
            }
        }
        last = index
    }
    output.appendHTML(text, last)
    return output.toString()
}

private fun StringBuilder.appendHTML(text: String, start: Int = 0, end: Int = text.length) {
    append(encodeHTMLEntities(text.substring(start, end)).replace("\n", "<br>"))
}

private fun AnnotatedString.Range<String>.toTag(alt: String? = null, close: Boolean = false): String {
    val altAttr = alt?.let { " alt=\"${encodeHTMLEntities(it)}\"" } ?: ""
    val attrs = if (item.isEmpty()) "" else " " + attrs.entries
        .filter { (k, _) -> !k.startsWith("*") }
        .joinToString(" ") { it.toAttribute() }
    val end = if (close && tag !in voidTags) "/" else ""
    return "<$tag$attrs$altAttr$end>"
}

private val tagHasAltText = setOf("img")
private val specialTags = setOf("#cdata-section", "#comment", "#declaration", "#processing-instruction")

private fun Map.Entry<String, Any>.toAttribute(): String {
    val (key, value) = this
    val k = encodeHTMLEntities(key)
    val v = encodeHTMLEntities(value.toString())
    return when (value) {
        false -> ""
        true, "" -> k
        is String -> "$k=\"$v\""
        else -> "$k=$v"
    }
}
