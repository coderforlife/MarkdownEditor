
/** Decode HTML entities in the string, e.g. &amp; or &#x3C;. */
fun decodeHTMLEntities(str: String): String {
    return htmlEntityMatch.replace(str) {
        val value = it.groupValues[1]
        when {
            value.startsWith("#x") || value.startsWith("#X") ->
                value.substring(2).toInt(16).toChar().toString()
            value.startsWith("#") ->
                value.substring(1).toInt(10).toChar().toString()
            else -> htmlEntities[value] ?: it.value
        }
    }
}



